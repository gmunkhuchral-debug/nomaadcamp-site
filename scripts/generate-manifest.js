// scripts/generate-manifest.js
// Auto-generates images/manifest.js by scanning image folders.
// Run: node scripts/generate-manifest.js
// Netlify and GitHub Actions run this automatically as the build command.

'use strict';

const fs     = require('fs');
const path   = require('path');
const crypto = require('crypto');

const ROOT      = path.join(__dirname, '..');
const CAMPS_DIR = path.join(ROOT, 'images', 'camps');
const HERO_DIR  = path.join(ROOT, 'images', 'hero');
const GAL_DIR   = path.join(ROOT, 'images', 'gallery');
const PARTNERS_DIR = path.join(ROOT, 'logos', 'partners');
const OUTPUT    = path.join(ROOT, 'images', 'manifest.js');

const CATEGORY_ORDER = ['stage', 'lounge', 'dining', 'setup', 'team', 'night'];

const IMAGE_EXTS = new Set(['.jpg', '.jpeg', '.png', '.webp', '.avif', '.gif', '.svg']);

function webPath(absPath) {
  return '/' + path.relative(ROOT, absPath).split(path.sep).join('/');
}

function listImages(dir) {
  if (!fs.existsSync(dir)) return [];
  return fs.readdirSync(dir)
    .filter(f => IMAGE_EXTS.has(path.extname(f).toLowerCase()))
    .sort()
    .map(f => webPath(path.join(dir, f)));
}

const hero    = listImages(HERO_DIR);
const gallery = listImages(GAL_DIR);
const partners = listImages(PARTNERS_DIR);

// Build gallery categories: each subfolder of images/gallery/ is a category.
// Each category may have a meta.json and image files (01.jpg, 02.jpg, …).
// cover.jpg is reserved as the tab thumbnail; all other images are gallery images.
const galleryCategories = {};
if (fs.existsSync(GAL_DIR)) {
  const catFolders = fs.readdirSync(GAL_DIR)
    .filter(name => fs.statSync(path.join(GAL_DIR, name)).isDirectory())
    .sort((a, b) => {
      const ai = CATEGORY_ORDER.indexOf(a);
      const bi = CATEGORY_ORDER.indexOf(b);
      if (ai === -1 && bi === -1) return a.localeCompare(b);
      if (ai === -1) return 1;
      if (bi === -1) return -1;
      return ai - bi;
    });

  catFolders.forEach(folder => {
    const catDir  = path.join(GAL_DIR, folder);
    const metaPath = path.join(catDir, 'meta.json');
    let meta = {};
    if (fs.existsSync(metaPath)) {
      try { meta = JSON.parse(fs.readFileSync(metaPath, 'utf8')); } catch (_) {}
    }

    const allImages = listImages(catDir);
    const images    = allImages.filter(p => !p.endsWith('/cover.jpg'));
    const coverFile = path.join(catDir, 'cover.jpg');
    const cover     = fs.existsSync(coverFile) ? webPath(coverFile) : (images[0] || null);

    galleryCategories[folder] = {
      title:       meta.title       || folder,
      label:       meta.label       || folder.toUpperCase(),
      description: meta.description || '',
      cover,
      images,
    };
  });
}

// Build camps: each subfolder of images/camps/ becomes a camp entry.
// Key is derived by stripping the trailing "-camp" suffix (e.g. "a-camp" → "a").
const camps = {};
if (fs.existsSync(CAMPS_DIR)) {
  fs.readdirSync(CAMPS_DIR)
    .filter(name => fs.statSync(path.join(CAMPS_DIR, name)).isDirectory())
    .sort()
    .forEach(folder => {
      const key     = folder.replace(/-camp$/, '');
      const cover   = listImages(path.join(CAMPS_DIR, folder, 'cover'));
      const gallery = listImages(path.join(CAMPS_DIR, folder, 'gallery'));
      camps[key] = {
        cover:   cover[0] || null,   // first image in cover/ is the card thumbnail
        gallery,                      // all images in gallery/ shown in carousel
      };
    });
}

const manifest = { hero, gallery, partners, galleryCategories, camps };

const out = [
  '// NOMAAD Image Manifest — auto-generated by scripts/generate-manifest.js',
  '// DO NOT edit by hand. To add images:',
  '//   cover image  → images/camps/<camp>/cover/<any-filename>',
  '//   gallery image → images/camps/<camp>/gallery/<any-filename>',
  '//   partner logos → logos/partners/<any-filename>',
  '// Then commit and push — Netlify regenerates this file on every deploy.',
  'window.NOMAAD_IMAGES = ' + JSON.stringify(manifest, null, 2) + ';',
  '',
].join('\n');

fs.writeFileSync(OUTPUT, out, 'utf8');

// Cache-bust the manifest <script> tag in index.html so browsers always
// fetch the new file when its content changes (GitHub Pages serves with
// Cache-Control: max-age=600, so without this browsers show stale images).
const hash      = crypto.createHash('sha256').update(out).digest('hex').slice(0, 8);
const indexPath = path.join(ROOT, 'index.html');
if (fs.existsSync(indexPath)) {
  const html    = fs.readFileSync(indexPath, 'utf8');
  const updated = html.replace(
    /<script src="\/images\/manifest\.js(?:\?[^"]*)?"><\/script>/,
    `<script src="/images/manifest.js?v=${hash}"></script>`
  );
  if (updated !== html) {
    fs.writeFileSync(indexPath, updated, 'utf8');
    console.log('index.html updated — manifest cache-bust hash: ' + hash);
  }
}

console.log('manifest written → ' + path.relative(process.cwd(), OUTPUT));
Object.entries(camps).forEach(([k, v]) =>
  console.log('  ' + k + '-camp: cover=' + (v.cover ? 1 : 0) + ', gallery=' + v.gallery.length)
);
Object.entries(galleryCategories).forEach(([k, v]) =>
  console.log('  gallery/' + k + ': images=' + v.images.length + ', cover=' + (v.cover ? 1 : 0))
);
